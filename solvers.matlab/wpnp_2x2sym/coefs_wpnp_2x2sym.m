function [C,U,dU] = coefs_wpnp_2x2sym(data)

    A = data{1};
    B = data{2};

    C = zeros(4,16);
    C([14,24,31,33]) = 64*A(3)^2;
    C([3,5,38,48]) = -32*A(1)^2+32*A(2)^2;
    C([12,17,26,43]) = 32*A(1)^2+32*A(2)^2;
    C([56,59]) = -8*A(3)*B(5)-8*1i*A(3)*B(6);
    C([50,61]) = 8*1i*A(3)*B(6)-8*A(3)*B(5);
    C(60) = -8*A(1)*B(1)+8*A(2)*B(4)-8*1i*A(1)*B(2)-8*1i*A(2)*B(3);
    C(49) = -8*A(1)*B(1)+8*A(2)*B(4)+8*1i*A(1)*B(2)+8*1i*A(2)*B(3);
    C(55) = 8*A(1)*B(1)+8*A(2)*B(4)+8*1i*A(1)*B(2)-8*1i*A(2)*B(3);
    C(62) = -8*1i*A(1)*B(2)+8*1i*A(2)*B(3)+8*A(1)*B(1)+8*A(2)*B(4);

    C = C./repmat(sqrt(sum(C.*conj(C),2)),1,size(C,2));

    U = @(S,n)[S(2,:).*S(1,:).^2.*S(3,:);S(2,:).*S(3,:).^2.*S(1,:);S(2,:).*S(1,:).^2.*S(4,:);S(2,:).*S(3,:).*S(4,:).*S(1,:);S(2,:).*S(4,:).^2.*S(1,:);S(3,:).*S(2,:).*S(1,:);S(3,:).^2.*S(2,:);S(4,:).*S(2,:).*S(1,:);S(4,:).*S(3,:).*S(2,:);S(4,:).^2.*S(2,:);S(2,:).*S(3,:);S(2,:).*S(4,:);S(1,:);S(3,:);S(4,:);ones(1,n)];
    dU = @(S,n)[[2.*S(3,:).*S(2,:).*S(1,:);S(3,:).^2.*S(2,:);2.*S(4,:).*S(2,:).*S(1,:);S(4,:).*S(3,:).*S(2,:);S(4,:).^2.*S(2,:);S(2,:).*S(3,:);zeros(1,n);S(2,:).*S(4,:);zeros(1,n);zeros(1,n);zeros(1,n);zeros(1,n);ones(1,n);zeros(1,n);zeros(1,n);zeros(1,n)],[S(1,:).^2.*S(3,:);S(3,:).^2.*S(1,:);S(1,:).^2.*S(4,:);S(3,:).*S(4,:).*S(1,:);S(4,:).^2.*S(1,:);S(3,:).*S(1,:);S(3,:).^2;S(4,:).*S(1,:);S(4,:).*S(3,:);S(4,:).^2;S(3,:);S(4,:);zeros(1,n);zeros(1,n);zeros(1,n);zeros(1,n)],[S(2,:).*S(1,:).^2;2.*S(3,:).*S(2,:).*S(1,:);zeros(1,n);S(4,:).*S(2,:).*S(1,:);zeros(1,n);S(2,:).*S(1,:);2.*S(2,:).*S(3,:);zeros(1,n);S(2,:).*S(4,:);zeros(1,n);S(2,:);zeros(1,n);zeros(1,n);ones(1,n);zeros(1,n);zeros(1,n)],[zeros(1,n);zeros(1,n);S(2,:).*S(1,:).^2;S(3,:).*S(2,:).*S(1,:);2.*S(4,:).*S(2,:).*S(1,:);zeros(1,n);zeros(1,n);S(2,:).*S(1,:);S(2,:).*S(3,:);2.*S(2,:).*S(4,:);zeros(1,n);S(2,:);zeros(1,n);zeros(1,n);ones(1,n);zeros(1,n)]];

end