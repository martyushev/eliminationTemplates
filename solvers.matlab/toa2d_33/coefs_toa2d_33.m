function [C,U,dU] = coefs_toa2d_33(data)

    dd = data{1};

    C = zeros(3,10);

    C([1,2,3]) = -2;
    C(19) = -2*dd(1);
    C(20) = -2*dd(2);
    C(21) = -2*dd(3);
    C(10) = -2*dd(4);
    C(11) = -2*dd(5);
    C(12) = -2*dd(6);
    C(4) = -2*dd(7);
    C(5) = -2*dd(8);
    C(6) = -2*dd(9);
    C(16) = 2*dd(4)+2*dd(1);
    C(17) = 2*dd(5)+2*dd(2);
    C(18) = 2*dd(6)+2*dd(3);
    C(13) = 2*dd(7)+2*dd(1);
    C(7) = 2*dd(7)+2*dd(4);
    C(14) = 2*dd(8)+2*dd(2);
    C(8) = 2*dd(8)+2*dd(5);
    C(15) = 2*dd(9)+2*dd(3);
    C(9) = 2*dd(9)+2*dd(6);
    C(28) = -2*dd(4)*dd(7)+2*dd(1)*dd(7)+2*dd(4)*dd(1)-2*dd(1)^2;
    C(25) = 2*dd(4)*dd(7)-2*dd(1)*dd(7)+2*dd(4)*dd(1)-2*dd(4)^2;
    C(22) = 2*dd(4)*dd(7)+2*dd(1)*dd(7)-2*dd(4)*dd(1)-2*dd(7)^2;
    C(29) = -2*dd(5)*dd(8)+2*dd(2)*dd(8)+2*dd(5)*dd(2)-2*dd(2)^2;
    C(26) = 2*dd(5)*dd(8)-2*dd(2)*dd(8)+2*dd(5)*dd(2)-2*dd(5)^2;
    C(23) = 2*dd(5)*dd(8)+2*dd(2)*dd(8)-2*dd(5)*dd(2)-2*dd(8)^2;
    C(30) = -2*dd(6)*dd(9)+2*dd(3)*dd(9)+2*dd(6)*dd(3)-2*dd(3)^2;
    C(27) = 2*dd(6)*dd(9)-2*dd(3)*dd(9)+2*dd(6)*dd(3)-2*dd(6)^2;
    C(24) = 2*dd(6)*dd(9)+2*dd(3)*dd(9)-2*dd(6)*dd(3)-2*dd(9)^2;

    C = C./repmat(sqrt(sum(C.*conj(C),2)),1,size(C,2));

    U = @(S,n)[S(1,:).*S(2,:).*S(3,:);S(1,:).^2;S(2,:).*S(1,:);S(2,:).^2;S(1,:).*S(3,:);S(3,:).*S(2,:);S(3,:).^2;S(1,:);S(2,:);S(3,:)];
    dU = @(S,n)[[S(3,:).*S(2,:);2.*S(1,:);S(2,:);zeros(1,n);S(3,:);zeros(1,n);zeros(1,n);ones(1,n);zeros(1,n);zeros(1,n)],[S(1,:).*S(3,:);zeros(1,n);S(1,:);2.*S(2,:);zeros(1,n);S(3,:);zeros(1,n);zeros(1,n);ones(1,n);zeros(1,n)],[S(2,:).*S(1,:);zeros(1,n);zeros(1,n);zeros(1,n);S(1,:);S(2,:);2.*S(3,:);zeros(1,n);zeros(1,n);ones(1,n)]];

end